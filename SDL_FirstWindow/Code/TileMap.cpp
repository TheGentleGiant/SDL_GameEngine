TileMap::TileMap()
{
	dirt = TextureManager::LoadTexture("./Asset/Dirt.png");
	grass = TextureManager::LoadTexture("./Asset/Grass.png");
	water= TextureManager::LoadTexture("./Asset/Water.png");


	LoadMap(lvl_one);

	source.x = source.y = 0;
	source.w = dest.w = 32;
	source.h = dest.w = 32;

	dest.x = dest.y = 0;

}


TileMap::~TileMap()
{
}

void TileMap::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25;column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void TileMap::DrawMap()
{
	int type = 0;
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(water, source, dest);
				if (!water)
				{
					printf("water is null \n");
				}
				break;
			case 1:
				TextureManager::Draw(grass, source, dest);
				break;
			case 2:
				TextureManager::Draw(dirt, source, dest);
				break;
			default:
				break;
			}
		}
	}
}
